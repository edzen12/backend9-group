# *args - arguments
# Название args — это соглашение, важна именно одиночная звездочка *, которая выполняет операцию "упаковки" аргументов. 
# *args в Python — для передачи произвольного количества позиционных аргументов в функцию, которые собираются в кортеж (tuple)
b = [6,7,8,9]
a = [1,2, *b,3,4,5]
print(a)
######
def calc(*args):
    result = 0
    for i in args:
        result+=i 
    print("ответ:",result)

calc(4,5,19,25)
