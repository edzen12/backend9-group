# **kwargs - keyword arguments - это специальный параметр функции
# двойная звездочка ** перед именем параметра (например, **kwargs) — это синтаксис, который преобразует эти аргументы в пары «ключ-значение», делая код более гибким
# dict - словарь {key:value}
def person(**kwargs):
    for key, value in kwargs.items():
        print(f"Ключи: {key}, Значение: {value}")

person(name='Али', dog='Жолборс', car='Camry', eats=['манты', "мясо", "бензин 92й"])
############
def kombo(*args, **kwargs):
    print("Имена:", args)
    print("Значения: ", kwargs) 

kombo('айжан', "Гена", age=33, height=177)